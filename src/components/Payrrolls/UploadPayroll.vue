<template>
  <div class="upload-payroll">
    <button class="ui button primary" @click="showCloseForm">
      Nueva nomina
    </button>
    <form
      class="ui form upload-payroll_form"
      :class="{ open: showForm }"
      @submit.prevent="handleSubmit"
    >
      <div class="field">
        <label for="file" class="ui icon button">
          <i class="file icon" />
          Seleccionar nomina
          <span v-if="file">({{ file.name }})</span>
        </label>
        <input
          type="file"
          id="file"
          style="display: none"
          @change="uploadFile"
        />
      </div>
      <div class="field">
        <div class="ui calendar">
          <div class="ui input left icon">
            <input type="date" @change="changeDate" :value="date" />
          </div>
        </div>
      </div>
      <button class="ui button positive">Subir nomina</button>
      <p v-if="error">{{ error }}</p>
    </form>
  </div>
</template>

<script>
import { ref } from "vue";
export default {
  name: "UpdatePayroll",
  setup() {
    let showForm = ref(false);
    let file = ref(null);
    let date = ref(null);
    let loading = ref(false);
    let error = ref(null);
    const showCloseForm = () => {
      showForm.value = !showForm.value;
    };
    const uploadFile = (e) => {
      const fileTemp = e.target.files[0];
      if (fileTemp.type === "application/pdf") {
        file.value = fileTemp;
      }
    };
    const changeDate = (e) => {
      date.value = e.target.value;
    };
    const handleSubmit = () => {
      console.log("Subiendo nomina");
    };
    return {
      showForm,
      showCloseForm,
      file,
      date,
      loading,
      error,
      uploadFile,
      handleSubmit,
      changeDate,
    };
  },
};
</script>

